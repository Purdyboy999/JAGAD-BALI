document.addEventListener('DOMContentLoaded', () => {
    // 0. Language Switcher
    const langBtnId = document.getElementById('lang-id');
    const langBtnEn = document.getElementById('lang-en');
    
    function setLanguage(lang) {
        // Update active classes
        if (lang === 'id') {
            langBtnId.classList.add('active');
            langBtnEn.classList.remove('active');
            document.documentElement.lang = "id";
        } else {
            langBtnEn.classList.add('active');
            langBtnId.classList.remove('active');
            document.documentElement.lang = "en";
        }
        
        // Update DOM elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // Update placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (translations[lang] && translations[lang][key]) {
                el.placeholder = translations[lang][key];
            }
        });
    }

    langBtnId.addEventListener('click', () => setLanguage('id'));
    langBtnEn.addEventListener('click', () => setLanguage('en'));
    // 1. Navbar Scroll Effect
    const navbar = document.getElementById('navbar');
    
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // 2. Mobile Menu Toggle
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');

    hamburger.addEventListener('click', () => {
        navLinks.classList.toggle('active');
        hamburger.classList.toggle('active');
    });

    // Close mobile menu when clicking a link
    document.querySelectorAll('.nav-links a').forEach(link => {
        link.addEventListener('click', () => {
            navLinks.classList.remove('active');
            hamburger.classList.remove('active');
        });
    });

    // 3. Smooth Scroll for Anchor Links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                // Adjust for fixed header
                const headerOffset = 80;
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.scrollY - headerOffset;
  
                window.scrollTo({
                    top: offsetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // 4. Scroll Animation (Intersection Observer)
    const fadeElements = document.querySelectorAll('.service-card, .section-header, .about-text, .stat-box, .contact-info, .contact-form-container');
    
    // Add base class for animation
    fadeElements.forEach(el => {
        el.classList.add('fade-in-up');
    });

    const appearOptions = {
        threshold: 0.15,
        rootMargin: "0px 0px -50px 0px"
    };

    const appearOnScroll = new IntersectionObserver(function(entries, observer) {
        entries.forEach(entry => {
            if (!entry.isIntersecting) {
                return;
            } else {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, appearOptions);

    fadeElements.forEach(el => {
        appearOnScroll.observe(el);
    });

    // 5. Toggle Service Types List
    const toggleBtns = document.querySelectorAll('.toggle-jenis');
    toggleBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            const targetId = btn.getAttribute('data-target');
            const targetList = document.getElementById(targetId);
            
            // Toggle active class on button (for arrow rotation)
            btn.classList.toggle('active');
            
            // Toggle open class on list (for expanding)
            targetList.classList.toggle('open');
        });
    });
});
